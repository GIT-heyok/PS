/*
Since there is no negative number,
Sort two arrays in opposite direction and get the sum.
*/
int main()
{
    int n;
    cin >> n;
    vector<int> a(n), b(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    
    for (int i = 0; i < n; i++)
    {
        cin >> b[i];
    }
    auto comp = [&](auto a1, auto a2){
        return a1>a2;
    };

    sort(a.begin(), a.end());
    sort(b.begin(), b.end(), comp);
    int ans = 0;
    for (int i = 0; i < n; i++)
    {
        ans+=a[i]*b[i];
    }
    cout<<ans<<endl;
    
}